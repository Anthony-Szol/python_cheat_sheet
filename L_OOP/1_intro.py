# OOPs
# Object Oriented Programming
# The principles of OOPs are based on 4 pillars apart from Class and Objects
# A class has attributes and behaviour

# Abstraction
# Means displaying only essential information and hiding the details
# Example: Car will accelerate but internal working of engine is not exposed

# Encapsulation
# Bundling of data into a single unit
# Bundling of all methods that can act on an object of that data
# Car can hav colors, brake, engineand a lot of other methods

# Inheritance
# When a class derives from another class
# Any new car company can inherit all the information from car class

# Polymorhism
# Means having many forms
# Same class methods can work differently for different objects