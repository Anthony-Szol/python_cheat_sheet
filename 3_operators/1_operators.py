# Subtraction
c = 3
d = 1
print(c-d)

# Multiply
e = 10
f = 2
print(e*f)

# Divide will always yield float value
g = 5
h = 5
print(g/h)

# You can add strings as well (String concatenation)
first = 'Anthony'
